public class L30_Round {
    public static void main(String[] args) {

        float num = 7.25f;

        System.out.println("Round result: " + Math.round(num));
        System.out.println("Floor result: " + Math.floor(num));
        System.out.println("Ceil result: " + Math.ceil(num));
    }
}
